-- ============================================================
-- レビューの重複投稿を防止する UNIQUE 制約を追加
-- 2026-04-25
--
-- 目的:
--   同一ユーザーが同じ施設に複数回レビューを投稿できないようにする。
--   これにより施設の評価（平均星評価）が特定ユーザーによって
--   不公平に操作されることを防ぐ。
--
-- 注意:
--   適用前に既存データに (user_id, facility_id) の重複がないか確認すること。
--   重複がある場合は先にクリーンアップが必要。
-- ============================================================

ALTER TABLE public.reviews
  ADD CONSTRAINT reviews_user_facility_unique
  UNIQUE (user_id, facility_id);
